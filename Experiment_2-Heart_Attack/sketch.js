function drawHeart(x, y, size) {
  fill(255, 0, 0); // Red fill
  beginShape();
  vertex(x, y); // Middle point
  bezierVertex(x + size / 2, y - size, x + size, y, x, y + size * 0.8);
  bezierVertex(x - size, y, x - size / 2, y - size, x, y);
  endShape(CLOSE);
}

